/* Cara kerja PHP Web
1. Web Browser akan melakukan HTTP Request ke Web Server 
2. Web Server menerima HTTP Request, lalu mengeksekusi file PHP yang sesuai dengan Request Tersebut
3. File PHP akan di eksekusi, lalu akan menjalankan kode program PHP, hasilnya akan dirender menjadi kontent
    sesuai dengan yang dilakukan oleh program
4. Hasil content yang dibuat PHP (misalnya HTML, CSS, JavaScript) akan dikembalikan ke Web Server
5. Web Server akan mengembalikan content tersebut sebagai HTTP Response ke Web Browser
6. Web Browser menerima content dari Web Server, lalu me-render content tersebut sesuai dengan tipe contentnya 
*/