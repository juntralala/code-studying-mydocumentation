public class operatorAsignment {
    public static void main(String[] args) {

        /*
        Augmented Assignment ada 5:
        1. +=
        2. -=
        3. *=
        4. /=
        5. %=
        */

        var a = 4;
        a += 4;
        System.out.println(a);
        a -= 2;
        System.out.println(a);
        a *= 7;
        System.out.println(a);
        a /= 3;
        System.out.println(a);
        a %= 7;
        System.out.println(a);

    }
}
