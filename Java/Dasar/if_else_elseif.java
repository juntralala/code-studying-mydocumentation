public class if_else_elseif {
    public static void main(String[] args) {

        int nilai = 75;

        if (nilai >= 70) {
            System.out.println("Anda Luslus");
        } else if (nilai >= 60){
            System.out.println("Anda mengikuti remidial");
        } else {
            System.out.println("Anda Tidak lulus");
        }

    }
}
