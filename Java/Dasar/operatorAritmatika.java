public class operatorAritmatika {

    public static void main(String[] args) {

        /*
        * operator aritmatika java ada 5:
        * 1. +
        * 2. -
        * 3. *
        * 4. /
        * 5. %
        */

        var a = 10;
        var b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }

}
