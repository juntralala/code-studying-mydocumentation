/** Apa itu Synchronous 
 * Program dalam JavaScript secara default akan dieksuki baris demi baris
 * Secara default, proses di JavaScript akan dieksekusi secara Synchronous, artinya baris selanjutnya akan
   akan dieksekusi setelah baris sebelumnya selesai dikerjakan
 * Proses Synchronous juga biasa disebut Blocking, karena harus menunggu tiap proses selesai, baru proses 
   selanjutnya bisa dilakukan
*/