/** Synchronous (CHAT-GPT3.5)
 * Synchronous merujuk pada model eksekusi program di mana setiap tugas atau pernyataan dieksekusi secara
   berurutan, satu per satu, dan program menunggu tugas yang sedang berlangsung untuk selesai sebelum
   melanjutkan ke tugas berikutnya. Dalam model ini, eksekusi berlangsung secara langsung dan sinkron,
   tanpa adanya perpindahan eksekusi ke tugas yang lain sampai tugas yang sedang berlangsung selesai.
 */