/** Tipe data yang ada di DOM
    +===============+=======================================================================+
    | Tipe Data     | Keterangan                                                            |
    +---------------+-----------------------------------------------------------------------+
    | Document      | Merupakan representasi dari sebuah halaman Web                        |
    | Node          | Merupakan base class untuk semua tipe data di DOM, seperti Document,  |
    |               |  Element, dan Attr                                                    |
    | Element       | Merupakan representasi object element di halaman web, misal : head,   |
    |               |  body, form, input, dan lain-lain                                     |
    | NodeList      | Merupakan Array / collection yang berisikan Node                      |
    | Attr          | atau attribute, merupakan representasi object attribute dalam element |
    | NamedNodeMap  | Merupakan collection yang berisikan Attr dalam bentuk Map             |
    +===============+=======================================================================+
*/
