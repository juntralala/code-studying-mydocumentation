/*
operator perbandingan dart ada 6 : == != < > <= >=
*/

void main() {

  print('10 == 10 : ${10 == 10}');
  print('10 != 10 : ${10 != 10}');
  print('9   < 10 : ${9  <  10}');
  print('9   > 10 : ${9  >  10}');
  print('10 =< 10 : ${10 <=  10}');
  print('10 >= 10 : ${10 >=  10}\n');

  print('ujun  == ujun : ${"ujun" == "ujun"}');
  print('ujun  != ujun : ${"ujun" != "ujun"}');

}