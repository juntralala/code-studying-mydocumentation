
Berbeda dengan CHAR DAN VARCHAR yang bisa kita tentukan panjang maximumnya, TEXT tidak bisa dan sudah memiliki maximum panjangnya

terdapat 4 tipe TEXT :
TINYTEXT dengan maximum 255 karakter (~256byte)
TEXT dengan maximum 65535 karakter (~64kb)
MEDIUMTEXT dengan maximum 16777215 karakter (~16MB)
LONGTEXT dengan maximum 4294967295 karakter (~4GB)